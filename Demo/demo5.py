import math
print('------------------------------返回多个值------------------------')
def move(x, y, setp, angle = 0):# angle 有默认值， 说明可以不传
    nx = x + setp * math.cos(angle)
    ny = y + setp * math.sin(angle)
    return nx ,ny

# Python函数返回的是单一值 不过多个返回值这种是返回一个tuple
print(move(100, 100, 60, math.pi / 6))


